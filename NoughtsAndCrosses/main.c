int a[5][5];// объявляем  текущее поле  3 строки и 3 столбца. 
//если  нет ничего то поле 0, если нолик,  то  1, если крестик то 2 
// обнуление поля

void nul()
{
    int i, j;
    //  пробегаем по всем строкам
    for (i = 1; i <= 5; i++) {
        // пробегаем по всем столбцам
        for (j = 1; j <= 5; j++) {
            a[i][j] = 0;
        }
    }
}

